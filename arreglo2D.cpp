// Ejemplo arreglo 2D

#include <iostream>
using namespace std;

int main()
{
    int ids [5][3] =
    {
      {11,22,23},
      {17,52,53},
      {18,82,38},
      {16,62,83},
      {14,92,93},
    };

    for(int fila = 0; fila < 5; fila++)
    {
       for(int columna = 0; columna < 3; columna++)
       {
         cout << " Fila "<< fila <<" Columna "<< columna << " = " << ids[fila][columna]<< ",";
       }
       cout << endl;
    }
    return 0;
}
